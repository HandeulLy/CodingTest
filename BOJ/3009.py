#####################################################

p, d = [input().split() for _ in range(3)], ["", ""]
for i in range(2) :
    if p[0][i] in p[1][i] : d[i] = p[2][i]
    elif p[0][i] in p[2][i] : d[i] = p[1][i]
    else : d[i] = p[0][i]
print(d[0],d[1])

