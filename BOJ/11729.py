'''
풀이

1) n=1 인 경우
    - 1에서 3으로 이동한다. 끝.

2) n=2 인 경우
    - 1에서 2로 이동한다.
    - 1에서 3으로 이동한다.
    - 2에서 3으로 이동한다. 끝.

3) n=3 인 경우
    - 1에서 3으로 이동한다.
    - 1에서 2로 이동한다.
    - 3에서 2로 이동한다.
    - 1에서 3으로 이동한다.
    - 2에서 1로 이동한다.
    - 2에서 3으로 이동한다.
    - 1에서 3으로 이동한다. 끝.

4) n=4 인 경우
    - 1에서 2로 이동한다.
    - 1에서 3으로 이동한다.
    - 2에서 3으로 이동한다.
    - 1에서 2로 이동한다.
    - 3에서 1로 이동한다.
    - 3에서 2로 이동한다.
    - 1에서 2로 이동한다.
    - 1에서 3으로 이동한다.
    - 2에서 3으로 이동한다.
    - 2에서 1로 이동한다.
    - 3에서 1로 이동한다.
    - 2에서 3으로 이동한다.
    - 1에서 2로 이동한다.
    - 1에서 3으로 이동한다.
    - 2에서 3으로 이동한다. 끝.
'''

def hanoi(n, a, b, c) : 
    if n== 1 :
        print(a, b)
        return
    hanoi(n-1, a, c, b)
    print(a, b)
    hanoi(n-1, c, b, a)

n = int(input())
print(2**n-1)
hanoi(n, 1, 3, 2)